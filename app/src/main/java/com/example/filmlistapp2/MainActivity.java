public class MainActivity extends AppCompatActivity {

    Button signIn, registration;
    TextView test;

    //List<Film> filmList = new ArrayList<>();

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);
        signIn = (Button) findViewById(R.id.MainSignIn);
        registration = (Button) findViewById(R.id.MainRegistration);
        test = (TextView) findViewById(R.id.Test);

        if ( !isOnline() ){
            Toast.makeText(this, "Отсутствует подключение к интернету", Toast.LENGTH_LONG).show();
            MainActivity.this.finish();
        }


        signIn.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                Intent intent = new Intent(MainActivity.this, SignIn.class);
                //intent.putExtra("filmList", (Parcelable) filmList);
                startActivity(intent);
            }
        });

        registration.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                Intent intent = new Intent(MainActivity.this, Registration.class);
                startActivity(intent);
            }
        });

    }

    protected boolean isOnline() {
        String cs = Context.CONNECTIVITY_SERVICE;
        ConnectivityManager cm = (ConnectivityManager)
                getSystemService(cs);
        if (cm.getActiveNetworkInfo() == null) {
            return false;
        } else {
            return true;
        }
    }

}